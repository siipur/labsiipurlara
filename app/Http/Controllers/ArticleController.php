    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        // get single articles
        $article = Article::findOrFail($id);
        // return single articles as a resources
        if($article->delete()){        
            return new ArticleResource($article);
        }
